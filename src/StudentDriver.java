public class StudentDriver {
    String age;
    int rank;

    public StudentDriver(String age, int rank) {
        this.age = age;
        this.rank = rank;
    }

    public String getAge() {
        return this.age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }
}
